package ch.nellen.silvan.games.polygon.graphics.impl;

public class RGBAColor {
	
	public RGBAColor(float r, float g, float b, float alpha) {
		super();
		this.r = r;
		this.g = g;
		this.b = b;
		this.alpha = alpha;
	}
	
	public float r = 0;
	public float g = 0;
	public float b = 0;
	public float alpha = 0;
}
